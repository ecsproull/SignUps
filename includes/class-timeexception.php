<?php
/*
 * Summary
 * TimeException class.
 */

/**
 * TimeException for rolling signups
 */
class TimeException {
	/**
	 * Template that it pertains to.
	 *
	 *  mixed
	 */
	public $template;
	/**
	 * Reason for the exception.
	 *
	 *  mixed
	 */
	public $reason;

	/**
	 * Begin time
	 *
	 *  mixed
	 */

	public $begin;

	/**
	 * End time
	 *
	 *  mixed
	 */
	public $end;

	/**
	 * End time
	 *
	 *  mixed
	 */
	public $delete;
}
